# ── Константи шрифту ─────────────────────────────────────────
SCALE  = 3   # кожен біт = SCALE×SCALE пікселів
CHAR_W = 5   # ширина символу в бітах
CHAR_H = 9   # висота символу в бітах
GAP    = 2   # відстань між символами в бітах


def text_width(text):
    """Повертає ширину рядка в реальних пікселях."""
    return len(text) * (CHAR_W + GAP) * SCALE


def draw_text(oled, text, x=0, y=0, brightness=15):
    """
    Малює рядок тексту піксельним шрифтом.

    Параметри:
        oled       — об'єкт OLED_1inch5
        text       — рядок для виводу
        x, y       — координати верхнього лівого кута
        brightness — яскравість 0–15
    """
    cx = x
    for ch in text:
        glyph = FONT.get(ch, FONT[' '])
        for row_i, row in enumerate(glyph):
            for col_i, bit in enumerate(row):
                if bit:
                    rx = cx + col_i * SCALE
                    ry = y  + row_i * SCALE
                    if -SCALE < rx < 128:
                        oled.rect(rx, ry, SCALE, SCALE, brightness, True)
        cx += (CHAR_W + GAP) * SCALE
